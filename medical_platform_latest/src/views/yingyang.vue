<h2>豆浆不宜与红糖鸡蛋同食</h2>
<div class="content">饮用豆浆可以提供给我们很丰富的钙和铁等元素，对我们身体有很多的好处，但是豆浆和鸡蛋红糖是不能一起食用的，如果一起食用不仅没有什么好处还会对我们的身体造成不小的伤害，所以并不建议你这样食用。最主要的原因是红糖，红糖是由甘蔗榨汁浓缩而成的带蜜糖，又被称为”东方巧克力“，红糖的成分与甘蔗成分基本相同，含有很多的苹果酸等有机酸性物质，这些有机酸性物质会和豆浆中的蛋白质相结合形成一些”变性沉淀物“如草酸钙等，这些物质不仅会降低豆浆和红糖本身的营养价值，还会抑制身体对于铁、锌等一些微量元素的吸收。
